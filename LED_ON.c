// Steps For Blink LED
// Final Output:
// TOGGLE THE LED AT PA5
// PA5 is Available in AHB1
// GPIOA is Located at 0x40020000 - 0x400203FF=> Total length of GPIOA is 1024 Bytes(1KB)
// EACH GPIO Has 4 32-Bit Configuration register(MODER, OTYPER, OSPEEDR, PUPDER), 2 32-Bit Data Register, 1 32-Bit Set/Reset Register, 1 32-Bit Locking Register, 2 32-Bit Alternate Function Register


// Things Need to Configure for the Toggle the GPIO
// MODER- CONFIGURE FOR INPUT/OUTPUT/ALTERNATE FUNCTION
// OTYPER- USED FOR PUSH PULL or OPEN DRAIN
// OSPEEDR- USED FOR SWITCHING SPEED
// PUPDR- PULL UP or PULL DOWN REGISTER


// GPIOA_MODER ADDRESS:
// FOR MODER ADDRESS OFFSET: 0x00
// FOR A PORT DEFAULT CONFIGURATION FOR SW: 1010 1000 0000 0000 0000 0000 0000 0000
// HERE THE MODER5 IS LOCATED AT 10, 11: For OUTPUT 10th Bit should be SET and 11th Bit should be RESET(01)


// GPIOA_OTYPER ADDRESS:
// FOR OTYPER ADDRESS OFFSET: 0x04
// FOR A PORT DEFAULT CONFIGURATION: 0000 0000 0000 0000 0000 0000 0000 0000
// HERE THE OTYPER5 LOCATED AT 5. BY DEFAULT IT IS IN PUSH PULL. SO NO CHANGE


// GPIOA_OSPEEDR ADDRESS:
// FOR OSPEEDR ADDRESS OFFSET: 0x08 
// FOR A PORT DEFAULT CONFIGURATION: 0000 0000 0000 0000 0000 0000 0000 0000
// HERE THE OSPEEDR5 LOCATED AT 10, 11: FOR LED BLINKING IT BE IN SLOW MODE(00)


// GPIOA_PUPDR ADDRESS:
// FOR GPIOA_PUPDR ADDRESS OFFSET: 0x0C
// FOR A PORT DEFAULT CONFIGURATION FOR SW: 0110 0100 0000 0000 0000 0000 0000 0000
// HERE THE PUPDR5 LOCATED AT 10, 11: FOR INITIALIZATION(00)- For OUTPUT MODE NO PULL_UP and NO PULL_DOWN


// FOR CHANGING THE GPIOA REGISTER WE NEED TO ENABLE THE CLOCK FOR AHB1
// RCC BASE ADDRESS: 0x4002 3800 - 0x4002 3BFF
// RCC_AHB1ENR- LOCATED AT OFFSET: 0x30


// GPIOA_ODR ADDRESS:
// FOR GPIOA_ODR  ADDRESS OFFSET: 0x14
// FOR PORT A DEFAULT CONFIGURATION: 0000 0000 0000 0000 0000 0000 0000 0000
// HERE THE ODR5 LOCATED AT 5: SET- LED_ON, RESET- LED_OFF


// STEPS FOR LED_ON:
// SET THE RCC_AHB1ENR.
// SET THE GPIOA_MODER as Required
// SET THE GPIOA_PUPDR as PULL_DOWN for initialization
// SET THE GPIO_ODR




#include<stdio.h>
#include<stdint.h>
#define GPIOA_BASE 0x40020000
#define RCC_BASE 0x40023800



#define RCC_AHB1ENR (*(volatile uint32_t *)(RCC_BASE+0x30))
#define GPIOA5_MODER (*(volatile uint32_t *)(GPIOA_BASE+0x00))
#define GPIOA5_PUPDR (*(volatile uint32_t *)(GPIOA_BASE+0x0C))
#define GPIOA5_ODR (*(volatile uint32_t*)(GPIOA_BASE+0x14))

#define GPIO 5

//For Setting an Nth Bit left Shift 1 by N and Do Or Operation
void GPIO_MODER_OUTPUT(){
	GPIOA5_MODER &= ~(3<<(GPIO*2));
	GPIOA5_MODER |= (1<<(GPIO*2));
}
// void GPIO_INIT(){
	// GPIOA5_PUPDR &= ~(3<<(GPIO*2));
// }
void RCC_ENR(){
	RCC_AHB1ENR |= (1<<0);
}
void LED_ON(){
	GPIOA5_ODR|=(1<<GPIO);
}
int main(){
	RCC_ENR();
	GPIO_MODER_OUTPUT();
	// GPIO_INIT();
	LED_ON();
	return 0;
}
